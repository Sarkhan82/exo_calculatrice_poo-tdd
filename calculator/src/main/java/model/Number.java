package model;

public class Number {

	// ---------------------------------------------------------------------------
	// ATTRIBUTS
	// ---------------------------------------------------------------------------
	private double value = 0;

	// ---------------------------------------------------------------------------
	// CONSTRUCTEUR
	// ---------------------------------------------------------------------------
	
	public Number(double number) {
		value = number;
	}

	// ---------------------------------------------------------------------------
	// GETTER & SETTER
	// ---------------------------------------------------------------------------

	public double getValue() {
		return value;
	}

	public void setValue(double value) {
		this.value = value;
	}

}
